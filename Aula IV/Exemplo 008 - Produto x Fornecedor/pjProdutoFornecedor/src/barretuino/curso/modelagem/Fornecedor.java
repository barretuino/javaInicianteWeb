package barretuino.curso.modelagem;

public class Fornecedor {
	//Atributos
	private int id;
	private String descricao;
	
	//Métodos
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getDescricao() {
		return descricao;
	}
	public void setDescricao(String descricao) {
		this.descricao = descricao;
	}
}